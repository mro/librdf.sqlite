// ANSI Terminal Text Colors
//
// http://stackoverflow.com/a/3219471
// http://www.tldp.org/HOWTO/Bash-Prompt-HOWTO/x329.html
// http://wiki.bash-hackers.org/scripting/terminalcodes
#define ANSI_COLOR_RESET "\x1b[0m"
#define ANSI_COLOR_F_GRAY "\x1b[1;30m"
#define ANSI_COLOR_F_RED "\x1b[1;31m"
#define ANSI_COLOR_F_GREEN "\x1b[1;32m"
#define ANSI_COLOR_F_YELLOW "\x1b[1;33m"
#define ANSI_COLOR_F_BLUE "\x1b[1;34m"
#define ANSI_COLOR_F_PURPLE "\x1b[1;35m"
#define ANSI_COLOR_F_CYAN "\x1b[1;36m"
#define ANSI_COLOR_F_WHITE "\x1b[1;37m"
#define ANSI_COLOR_B_GRAY "\x1b[7;30m"
#define ANSI_COLOR_B_RED "\x1b[7;31m"
#define ANSI_COLOR_B_GREEN "\x1b[7;32m"
#define ANSI_COLOR_B_YELLOW "\x1b[7;33m"
#define ANSI_COLOR_B_BLUE "\x1b[7;34m"
#define ANSI_COLOR_B_PURPLE "\x1b[7;35m"
#define ANSI_COLOR_B_CYAN "\x1b[7;36m"
#define ANSI_COLOR_B_WHITE "\x1b[7;37m"
